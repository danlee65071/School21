#include <stdlib.h>

void	free_arr(char	**map, int	**int_map, int high)
{
	int	i;

	i = 0;
	while (i < high + 1)
		free(map[i++]);
	free(map);
	i = 0;
	while (i < high)
		free(int_map[i++]);
	free(int_map);
}
